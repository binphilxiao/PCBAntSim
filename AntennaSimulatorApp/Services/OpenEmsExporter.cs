            sb.AppendLine("    f.write(f'Prad_W,{Prad:.6e}\\n')");
            sb.AppendLine("    f.write(f'Pin_W,{P_in:.6e}\\n')");
            sb.AppendLine("print(f'Far-field summary saved to {summary_csv}')");
